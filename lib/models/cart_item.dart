class CartItem {
  final String id;
  final String productId;
  final String name;
  final String imageUrl;
  final int quantidade;
  final double price;
  
  CartItem({
    required this.id,
    required this.productId,
    required this.name,
    required this.imageUrl,
    required this.quantidade,
    required this.price,
  });

 
}
